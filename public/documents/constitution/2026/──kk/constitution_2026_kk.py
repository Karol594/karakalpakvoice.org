def build_pdf(output_path="constitution_2026_kk.pdf"):
    doc = BaseDocTemplate(
        output_path,
        pagesize=A4,
        leftMargin=MARGIN,
        rightMargin=MARGIN,
        topMargin=MARGIN,
        bottomMargin=MARGIN,
        title=f"Қарақалпақстан Республикасының Конституциясы {YEAR}",
        author="www.karakalpakvoice.org",
        subject="Қарақалпақстан Республикасының Конституциясы",
        creator="www.karakalpakvoice.org"
    )
    doc.addPageTemplates([make_page_template(doc)])

    story = []
    story += build_cover()
    story += build_content()

    doc.build(story)
    print(f"✅ PDF жасалды: {output_path}")

