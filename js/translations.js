let currentLanguage = 'ru';

// DOM –∂“Ø–∫–ª–µ–Ω–≥–µ–Ω–Ω–µ–Ω –∫–µ–π–∏–Ω —Ç–∏–ª —Å–∏—Å—Ç–µ–º–∞—Å—ã–Ω –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è–ª–∞—û
document.addEventListener('DOMContentLoaded', function() {
    initializeTranslationSystem();
});

// –ê—û–¥–∞—Ä–º–∞ —Å–∏—Å—Ç–µ–º–∞—Å—ã–Ω –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è–ª–∞—û
function initializeTranslationSystem() {
    // LocalStorage-—Ç–∞–Ω —Ç–∏–ª–¥–∏ –æ“õ—ã—û
    const savedLanguage = localStorage.getItem('selectedLanguage');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
    }
    
    // –¢–∏–ª —Ç–∞“£–ª–∞—û—à—ã–Ω—ã –æ—Ä–Ω–∞—Ç—ã—û
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
        languageSelect.addEventListener('change', function() {
            changeLanguage(this.value);
        });
    }
    
    // –î”ô—Å–ª–µ–ø–∫–∏ –∞—û–¥–∞—Ä–º–∞–Ω—ã “õ–æ–ª–ª–∞–Ω—ã—û
    applyTranslations();
}

// –¢–∏–ª–ª–∏ ”©–∑–≥–µ—Ä—Ç–∏—û —Ñ—É–Ω–∫—Ü–∏—è—Å—ã
function changeLanguage(languageCode) {
    if (!translations[languageCode]) {
        console.error('–ê—û–¥–∞—Ä–º–∞ —Ç–∞—û—ã–ª–º–∞–¥—ã:', languageCode);
        return;
    }
    
    currentLanguage = languageCode;
    localStorage.setItem('selectedLanguage', languageCode);
    
    // –ê—û–¥–∞—Ä–º–∞–ª–∞—Ä–¥—ã “õ–æ–ª–ª–∞–Ω—ã—û
    applyTranslations();
    
    // URL-–¥—ñ –∂–∞“£–∞—Ä—Ç—ã—û
    updateLanguageInURL(languageCode);
    
    // Custom event –∂–∏–±–µ—Ä–∏—û
    document.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language: languageCode }
    }));
}

// –ê—û–¥–∞—Ä–º–∞–ª–∞—Ä–¥—ã “õ–æ–ª–ª–∞–Ω—ã—û
function applyTranslations() {
    const lang = translations[currentLanguage];
    if (!lang) return;
    
    // Data-i18n –∞—Ç—Ä–∏–±—É—Ç–ª–∞—Ä—ã –±–∞—Ä —ç–ª–µ–º–µ–Ω—Ç—Ç–µ—Ä–¥–∏ –∞—û–¥–∞—Ä—ã—û
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (lang[key]) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = lang[key];
            } else if (element.tagName === 'INPUT' && element.placeholder !== undefined) {
                element.placeholder = lang[key];
            } else {
                element.textContent = lang[key];
            }
        }
    });
    
    // –ê—Ä–Ω–∞–π—ã —ç–ª–µ–º–µ–Ω—Ç—Ç–µ—Ä–¥–∏ –∞—û–¥–∞—Ä—ã—û
    updateSpecificElements(lang);
}

// –ê—Ä–Ω–∞–π—ã —ç–ª–µ–º–µ–Ω—Ç—Ç–µ—Ä–¥–∏ –∂–∞“£–∞—Ä—Ç—ã—û
function updateSpecificElements(lang) {
    // Hero section
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) heroTitle.textContent = lang.heroTitle;
    
    const heroSubtitle = document.querySelector('.hero-subtitle');
    if (heroSubtitle) heroSubtitle.textContent = lang.heroSubtitle;
    
    // Navigation links
    const navLinks = {
        'index.html': lang.home,
        'pages/sovereignty.html': lang.sovereignty,
        'pages/news.html': lang.news,
        'pages/politics.html': lang.politics,
        'pages/history.html': lang.history,
        'pages/sports.html': lang.sports,
        'pages/geography.html': lang.geography,
        'pages/traditions.html': lang.traditions,
        'pages/religion.html': lang.religion
    };
    
    document.querySelectorAll('.mobile-nav a').forEach(link => {
        const href = link.getAttribute('href');
        if (navLinks[href]) {
            link.textContent = navLinks[href];
        }
    });
    
    // Language selector options
    const languageOptions = document.querySelectorAll('#languageSelect option');
    languageOptions.forEach(option => {
        switch(option.value) {
            case 'ru':
                option.textContent = lang.russian;
                break;
            case 'kaa':
                option.textContent = lang.karakalpak;
                break;
            case 'en':
                option.textContent = lang.english;
                break;
            case 'pl':
                option.textContent = lang.polish;
                break;
        }
    });
    
    // Update document title
    document.title = `${lang.siteName} - ${lang.heroSubtitle.substring(0, 50)}...`;
}

// URL-–¥–µ —Ç–∏–ª–¥–∏ –∂–∞“£–∞—Ä—Ç—ã—û
function updateLanguageInURL(languageCode) {
    const url = new URL(window.location);
    url.searchParams.set('lang', languageCode);
    window.history.replaceState({}, '', url);
}

// URL-–¥–µ–Ω —Ç–∏–ª–ª–∏ –æ“õ—ã—û
function getLanguageFromURL() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('lang');
}

